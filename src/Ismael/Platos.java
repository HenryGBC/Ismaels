    public void llenarPlatos()
    {
        String []Datos = new String[4];
       Connection c = null;
       Statement stmt = null;
       try{
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:"+Ruta+"Ismael.sqlite");
            c.setAutoCommit(false);
            System.out.println("Opened database successfully");
            
            stmt = c.createStatement();
            ResultSet rs = stmt.executeQuery( "SELECT * FROM PLATOS;" );
            
         while ( rs.next() ) {
                int id = rs.getInt("ID_P");
                String name = rs.getString("NOMBRE");
                String apellido = rs.getString("DESCRIPCION");
                float precio = rs.getFloat("PRECIO");
                Datos[0] = Integer.toString(id);
                Datos[1] = name;
                Datos[2] = apellido;
                Datos[3] = Float.toString(precio);
                modelo.addRow(Datos);
        }
         rs.close();
        stmt.close();
        c.close();
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
    }
    public void removerFilasTabla()
   {
      int cantidadFilas = jTable1.getRowCount();
       for (int i = cantidadFilas-1; i >=0; i--) {
            modelo.removeRow(i);
           
       }
   }
