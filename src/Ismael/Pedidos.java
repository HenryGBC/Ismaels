                int id_c = rs.getInt("ID_C");
                int id_p = rs.getInt("ID_P");
                String Fecha = rs.getString("FECHA");
                int Cantidad = rs.getInt("CANTIDAD");
                String Total = rs.getString("TOTAL");
                int Entregado = rs.getInt("Entregado");
                Datos[0] = Integer.toString(id);
